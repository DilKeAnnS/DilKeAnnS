// Current language (default English)
let currentLanguage = 'en';

// Function to change language
function changeLanguage(langCode) {
    if (!translations[langCode]) {
        console.error('Language not supported:', langCode);
        return;
    }
    currentLanguage = langCode;
    localStorage.setItem('career_saathi_language', langCode);
    updatePageLanguage();
}

// Function to get translation
function t(key) {
    return translations[currentLanguage][key] || translations['en'][key] || key;
}

// Function to update entire page language
function updatePageLanguage() {
    // Update all elements with data-i18n attribute
    document.querySelectorAll('[data-i18n]').forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (element.tagName === 'INPUT' && element.placeholder) {
            element.placeholder = t(key);
        } else if (element.tagName === 'IMG') {
            element.alt = t(key);
        } else {
            element.textContent = t(key);
        }
    });
    
    // Update dynamic content
    const welcomeMsg = document.getElementById('welcomeMsg');
    if (welcomeMsg) {
        const hours = new Date().getHours();
        let greeting = '';
        if (hours < 12) greeting = '🌅 ' + t('welcome');
        else if (hours < 17) greeting = '☀️ ' + t('welcome');
        else greeting = '🌙 ' + t('welcome');
        welcomeMsg.innerHTML = greeting;
    }
}

// Load saved language on page load
function loadSavedLanguage() {
    const savedLang = localStorage.getItem('career_saathi_language');
    if (savedLang && translations[savedLang]) {
        currentLanguage = savedLang;
    }
    updatePageLanguage();
}

// Make functions globally available
window.changeLanguage = changeLanguage;
window.t = t;
window.loadSavedLanguage = loadSavedLanguage;

// Auto-load on page load
if (typeof window !== 'undefined') {
    document.addEventListener('DOMContentLoaded', loadSavedLanguage);
}
